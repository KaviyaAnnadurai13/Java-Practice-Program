package condition;

public class upperlowercase {
	public static void main(String[] args) {
		String s1="kaviya";
	    String s2="KAVIYA";
	    if(s1.equals(s2)) {
	    	System.out.println(s1+" is in upper case");
	    	
	    }
	    else {
	    	System.out.println(s1+" is in lower case");
	    }
	}

}
